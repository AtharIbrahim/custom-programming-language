#include <iostream>
using namespace std;

int main() {
    int a = 10;
    int b = 20;
    int sum = a + b;
    int diff = a - b;
    int product = a * b;
    int quotient = b / a;
    
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "sum = " << sum << endl;
    cout << "difference = " << diff << endl;
    cout << "product = " << product << endl;
    cout << "quotient = " << quotient << endl;
    
    return 0;
}